package com.chiefmech.arms.entity;

public class Unit {
	private String txtUnitId;
	private String txtUnitName;
	private String ddlStopFlag;
	private String txtRemarks;

	public String getTxtUnitId() {
		return txtUnitId;
	}

	public void setTxtUnitId(String txtUnitId) {
		this.txtUnitId = txtUnitId;
	}

	public String getTxtUnitName() {
		return txtUnitName;
	}

	public void setTxtUnitName(String txtUnitName) {
		this.txtUnitName = txtUnitName;
	}

	public String getDdlStopFlag() {
		return ddlStopFlag;
	}

	public void setDdlStopFlag(String ddlStopFlag) {
		this.ddlStopFlag = ddlStopFlag;
	}

	public String getTxtRemarks() {
		return txtRemarks;
	}

	public void setTxtRemarks(String txtRemarks) {
		this.txtRemarks = txtRemarks;
	}

}
